            <H2>REQUISITOS PARA EXPORTAR FLORES</H2>
            <P>PASO 1
            VERIFICAR ESTATUS FITOSANITARIO: Verificar si el país de destino ha levantado las 
            restricciones sanitarias de acceso a los productos de origen vegetal ecuatorianos a 
            través del Estatus Fitosanitario. ¿Qué es un Estatus Fitosanitario? Consiste en la 
            información que detalla la situación sanitaria y fitosanitaria de las flores en el 
            Ecuador para revisión y aprobación del país de destino (análisis de riesgo de plagas, 
            cultivo, etc.). Ver en sitio web de Agrocalidad:
            https://guia.agrocalidad.gob.ec/agrodb/aplicaciones/publico/productos1/consultaRequisitoComercio.php
            PASO 2
            REGISTRO EN AGROCALIDAD: Solicitar el Registro de Operador en la página web de Agrocalidad 
            a través del Sistema Guía: https://guia.agrocalidad.gob.ec/agrodb/ingreso.php
            En dicho sistema deberá ingresar la información de los sitios de operación (lugar de 
            producción, centro de acopio, centro de procesamiento, etc.) y la información de los 
            proveedores.
            PASO 3
            INSPECCIÓN: Un auditor de Agrocalidad realiza la inspección del lugar detallado en el 
            croquis entregado y se realizará un informe el cual debe ser aprobado por Agrocalidad. 
            Una vez aprobado el informe, se emite al usuario un certificado de registro y un código 
            de registro los cuales avalan como Operador en Agrocalidad (Este registro es reconocido 
            internacionalmente).
            PASO 4
            SOLICITAR CERTIFICADO FITOSANITARIO: Previo al embarque de las flores, se requiere el 
            Certificado Fitosanitario, uno por cada exportación y se lo debe solicitar máximo dos días 
            antes del despacho de la misma. La inspección se realiza en el punto de control (aeropuertos, 
            puertos marítimos, pasos fronterizos) o en las coordinaciones provinciales de Agrocalidad en 
            el caso de una pre-inspección.
            En el caso de pre-inspección, se emitirá un certificado provisional que se canjeará por el 
            Certificado Fitosanitario en el punto de control.
            En el caso de una inspección una vez aprobada se otorgará el Certificado Fitosanitario para 
            el envío a realizarse.
            Para más información, consultar el Acuerdo 390, el cual detalla el Programa de Certificación 
            Fitosanitaria de Ornamentales de Exportación (PCFOE), el cual establece procedimientos y 
            artículos reglamentados para la exportación de Productos Ornamentales.
            Link: https://www.agrocalidad.gob.ec/wp-content/uploads/2020/05/orn5.pdf
            PASO 5
            Solicitar certificado de origen, en caso de ser necesario en Agrocalidad, por medio del 
            Ecuapass.
            DOCUMENTOS REQUERIDOS PARA EXPORTAR: 
            -	Registro como operador de Comercio Exterior (Ecuapass) 
            -   copia del manifiesto de embarque, factura proforma u otro documento que indique 
            información del envío al país de destino.
            </P>
