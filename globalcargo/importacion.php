    aduanera, cuando estos sean exigidos.
    Los documentos de control previo son:
•	Registro sanitario.
•	Registro fitosanitario
•	Registro zoosanitario
•	INEN
•	MIPRO
</P>
<h1>PROVEEDORES</h1>
<H2>¿Cómo encontrar proveedores para importar?</H2>
<!-- aqui va la imagen que se llama proveedores.jpg -->
<P>Un proveedor es aquella persona natural o jurídica que se dedica a proveer o abastecer productos,
    mientras que buen proveedor es quien proporciona bienes o servicios de calidad, a un buen precio 
    y con buenos tiempos de entrega. 
    Localizar a buenos proveedores ya sean fabricantes o mayoristas es complejo pero los podemos 
    encontrar en:
•	Paginas web
•	Paginas amarillas
•	Eventos y exposiciones del producto que busca
•	LinkedIn, es una plataforma donde puede encontrar empresarios mayoristas
   Después de haber encontrado un proveedor, se recomienda encontrar más de uno, se debe analizar a 
   cada proveedor para verificar si los productos que oferta son de calidad y es realmente lo que se
   esta buscando como importador. 
   Variables a analizar para seleccionar el proveedor:
•	El precio es un punto importante para la elección de proveedores es la asequibilidad eso lo 
podemos definir después de responder a la pregunta ¿Sus precios son competitivos? Si sus precios son 
competitivos después de realizar un análisis en relación de la competencia realmente un Proveedor con 
precios competitivos es una opción atractiva.
•	También es importante contar con un proveedor fiable pero ¿Cómo saber si un proveedor es de 
confianza? Un proveedor es fiable o de confianza porque tienen siempre stock en sus bodegas, y sistemas 
de producción continua para asegurarse de tener suministro suficiente si algo sale mal. Sin embargo, 
en ocasiones se puede desarrollar una relación más cercana con ciertos proveedores, especialmente los 
pequeños proveedores.
•	El proveedor que se seleccione debe contar con una amplia experiencia dentro del mundo de comercio 
exterior, eso lo determinamos a partir del tiempo que lleva comercializando y la reputación que tiene 
esta debe ser buena. La experiencia es importante, especialmente si se quiere entablar una relación por 
muchos años con un proveedor
•	La ubicación de los proveedores es un factor muy importante a estudiar debido a que la ubicación 
del proveedor determinara el medio de transporte que se va a utilizar y el costo que este tendrá. 
Tratar con proveedores distantes de los puertos de importaciones y exportaciones puede significar 
mayores tiempos de entrega y costos adicionales de flete.
</P>
