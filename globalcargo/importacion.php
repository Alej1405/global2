?><h1>RÉGIMENES ADUANEROS</h1>
<!-- aqui va la imagen que se llama regimenes_aduaneros.jpg -->
<P>Los regímenes de importación son tratamientos aduaneros aplicables a las 
    mercancías provenientes del extranjero, solicitados por el declarante de 
    acuerdo con la legislación aduanera vigente, toda mercancía para 
    que pueda ingresar a Ecuador debe declarar su destino.
</P>
<h2>Importación para el consumo</h2>
<p>Régimen aduanero por el cual las mercancías importadas desde el extranjero 
o desde una Zona Especial de Desarrollo Económico pueden circular 
libremente en el territorio aduanero, con el fin de permanecer en él de 
manera definitiva, luego del pago de los derechos e impuestos a la importación, 
recargos y sanciones, cuando hubiere lugar a ellos, y del cumplimiento de las 
formalidades y obligaciones aduaneras. 
</p>
<h2>Admisión temporal para reexportación en el mismo estado</h2>
<p>Régimen aduanero que permite el ingreso al territorio aduanero de determinadas 
    mercancías importadas, para ser utilizadas en un fin determinado, con 
    suspensión total o parcial del pago de los derechos e impuestos a la 
    importación y recargos, con excepción de la depreciación normal originada 
    por el uso que se haya hecho de las mismas, para ser reexportadas en un plazo 
    determinado sin experimentar modificación alguna, según se determine en el 
    reglamento.
    Podrán ingresar bajo este régimen las mercancías que sean destinadas a los 
    siguientes fines o cumplan con las siguientes condiciones:
    1.	Para exposiciones, congresos, eventos.
    <!-- aqui va la imagen que se llama eventos.jpg -->
    2.	Para uso industrial, como moldes y matrices.
    <!-- aqui va la imagen que se llama moldes.jpeg -->
    3.	Actividades de turismo internacional efectuadas con medios de transporte 
    privados.
    <!-- aqui va la imagen que se llama transporte.jpg -->
    4.	Para realizar obras o prestación de servicios públicos en virtud de 
    contratos celebrados con instituciones del sector público o con empresas 
    privadas autorizadas para prestar servicios públicos.
    <!-- aqui va la imagen que se llama maquinas.jpg -->
    5.	Herramientas o equipos de reparación o mantenimiento a ser realizadas por 
    técnicos, contratadas por instituciones públicas o privadas.
    <!-- aqui va la imagen que se llama mantenimiento.jpg -->
    6.	Bienes de capital que ingresen al país con un contrato de arrendamiento 
    mercantil.
    <!-- aqui va la imagen que se llama bienes_de_capital.jpg -->
    7.	Naves amparadas en contratos de asociación debidamente autorizados por el 
    Estado, para realizar actividades pesqueras. 
    <!-- aqui va la imagen que se llama pesca.jpg -->
    Nota: Las mercancías que ingresen al país bajo el régimen de admisión temporal 
    de reexportacion en el mismo estado, no podrán ser comercializadas.
</p>
<h2>Admisión temporal para perfeccionamiento activo</h2>
<p>Régimen aduanero que permite el ingreso al territorio aduanero ecuatoriano, con 
    suspensión del pago de los derechos e impuestos a la importación y recargos 
    aplicables, de mercancías destinadas a ser exportadas luego de haber sido 
    sometidas a una operación de perfeccionamiento, bajo la forma de productos 
    compensadores. 
    Los productos compensadores que se obtengan aplicando este régimen podrán ser objeto 
    de cambio de régimen a importación para el consumo, pagando tributos sobre el 
    componente importado de dicho producto compensador.
</p>
<h2>Reposición de mercancías con franquicia arancelaria</h2>
<p>Régimen que permite importar, con exoneración de los derechos e impuestos a la 
    importación y recargos con excepción de las tasas aplicables, mercancías idénticas 
    o similares por su especie, calidad y sus características técnicas, a las que estando 
    en libre circulación, hayan sido utilizadas para obtener las mercancías exportadas 
    previamente con carácter definitivo.
</p>
<h2>Transformación bajo control aduanero</h2>
<p>Este régimen permite introducir en el territorio aduanero mercancías para someterlas 
    a operaciones que modifiquen su especie o estado, con suspensión del pago de los 
    derechos e impuestos a la importación y recargos aplicables, para la posterior 
    importación para el consumo de los productos resultantes obtenidos de esas operaciones, 
    con la aplicación de los derechos e impuestos a la importación y recargos que les 
    correspondan con arreglo a la naturaleza arancelaria del producto terminado.
</p>
<h2>Depósito aduanero</h2>
<!-- aqui va la imagen que se llama deposito.jpg -->
<p>Régimen aduanero según el cual las mercancías importadas son almacenadas por un periodo 
    determinado bajo el control de la aduana en un lugar habilitado y reconocido para esta 
    finalidad, sin el pago de los derechos e impuestos y recargos aplicables.
</p>
<h2>Reimportación en el mismo estado</h2>
<p>Es el régimen que permite la importación para el consumo con exoneración de los derechos 
    e impuestos a la importación, recargos aplicables de las mercancías que han sido exportadas, 
    a condición que no hayan sido sometidas a ninguna transformación, elaboración o reparación 
    en el extranjero y a condición que todas las sumas exigibles en razón de un reembolso o de 
    una devolución, de una exoneración condicional de derechos e impuestos o de toda subvención 
    u otro monto concedido en el momento de la exportación.</p>

    <h1>DOCUMENTOS DE CONTROL PREVIO</h1>
    <!-- aqui va la imagen que se llama dcp.jpg -->
<P>Los DCP son los registros, autorizaciones, permisos y certificados que deben cumplir ciertas 
    mercancías para su posterior ingreso a territorio ecuatoriano.
    Estos documentos deben estar aprobados por las entidades de control según las restricciones 
    indicadas en el arancel nacional el cual puede ser revisado en el sistema informático de 
